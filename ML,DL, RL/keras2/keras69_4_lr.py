# weight 값 조절
# 원래 y 값(0.8)을 찾아가는 과정

x_train = 0.5
y_train = 0.8   # 원래 y 값

########## 이부분 변경해보기 ##########
weight = 1.0
lr = 0.05
epoch = 200
########## 이부분 변경해보기 ##########

for iteration in range(epoch) :
    y_predict = x_train * weight        # y값
    error = (y_predict - y_train) ** 2  # loss : 원래 y 값과 예측한 y값의 차이 (mse와 유사한 개념)

    print("Error : " + str(error) + "\ty_predict : " + str(y_predict))

    up_y_predict = x_train * (weight + lr)          
    up_error = (y_train - up_y_predict) ** 2

    down_y_predict = x_train * (weight - lr)
    down_error = (y_train - down_y_predict) ** 2

    # loss 값이 작은 걸로 WEIGHT 값 갱신
    if (down_error <= up_error) :
        weight = weight - lr    # WEIGHT 값이 작아진다.
    if (down_error > up_error) :
        weight = weight + lr    # WEIGHT 값이 커진다.

