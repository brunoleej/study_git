# 동일한 형태로 나누기
import numpy as np

a = np.floor(10*np.random.random((2, 12)))

print(a)
'''
[[7. 1. 2. 9. 1. 0. 8. 8. 8. 9. 7. 9.]
 [8. 8. 4. 4. 5. 2. 7. 7. 8. 3. 7. 7.]]
'''
print(np.hsplit(a, 3))
'''
[array([[7., 1., 2., 9.],
       [8., 8., 4., 4.]]), array([[1., 0., 8., 8.],
       [5., 2., 7., 7.]]), array([[8., 9., 7., 9.],
       [8., 3., 7., 7.]])]
'''