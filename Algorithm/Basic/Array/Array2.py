# 파이썬과 배열
    # 파이썬 리스트 활용
# 1차원 배열 리스트로 구현 시
data = [1, 2, 3, 4, 5]
print(data) # [1, 2, 3, 4, 5]

# 2차원 배열 : 리스트로 구현 시
data = [[1,2,3],[4,5,6],[7,8,9]]
print(data) # [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

print(data[0])  # [1, 2, 3]

print(data[0][0])   # 1
print(data[0][1])   # 2
print(data[0][2])   # 3
print(data[1][0])   # 4